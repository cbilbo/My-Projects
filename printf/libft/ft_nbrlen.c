#include "libft.h"

int		ft_nbrlen(long nbr)
{
	int len;

	len = 0;
	if (nbr < 0)
	{
		nbr = -nbr;
		len++;
	}
	if ((nbr / 10) != 0)
		len += ft_nbrlen(nbr / 10);
	len++;
	return (len);
}
